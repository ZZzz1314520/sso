//用户类
public class User {
    private String name;
    private String pwd;
    private String id;
    public String getName(){
        return name;
    }
    public String getPwd(){
        return pwd;
    }
    public String getId(){
        return id;
    }
    public void setName(String s) {
        name = s;
    }
    public void setPwd(String s) {
        pwd = s;
    }
    public void setId(String s) {
        id = s;
    }
}
